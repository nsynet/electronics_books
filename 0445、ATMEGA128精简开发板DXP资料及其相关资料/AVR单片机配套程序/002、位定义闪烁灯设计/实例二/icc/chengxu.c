#include <iom128.h>
#include <macros.h>
/**********************************************************************
				  函数数据类型说明							   	
**********************************************************************/
#define uchar unsigned char 
#define uint unsigned int

/**********************************************************************
				   延时函数						   	
**********************************************************************/
void delay(uint MS)		  
{
 uint i,j;
 for( i=0;i<MS;i++)
 for(j=0;j<1141;j++);	//1141是在8MHz晶振下，通过软件仿真反复实验得到的数值
}
/**********************************************************************
				主函数						   	
**********************************************************************/
void main()
{
     DDRD=0XFF;//PD口作为输出口
	 while(1)
     {
	    PORTD|=BIT(3);//00001000 第四个口输出高电平
     	delay(1000); //延时
		PORTD&=~BIT(3);//00001000 第四个口输出低电平
		delay(1000); //延时
     }

}
/**********************************************************************
				 结束					   	
**********************************************************************/