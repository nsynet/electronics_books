#include <iom8v.h>
#include <macros.h>
/**********************************************************************
				  函数数据类型说明							   	
**********************************************************************/
#define uchar unsigned char 
#define uint unsigned int

/**********************************************************************
				   延时函数						   	
**********************************************************************/
void delay(uint MS)		  
{
 uint i,j;
 for( i=0;i<MS;i++)
 for(j=0;j<1141;j++);	//1141是在8MHz晶振下，通过软件仿真反复实验得到的数值
}
/**********************************************************************
				主函数						   	
**********************************************************************/
void main()
{
     DDRB=0XFF;
	 //PORTB=0XFF;
     while(1)
     {
	    PORTB|=BIT(3);//00001000
     	delay(1000);
		PORTB&=~BIT(3);
		delay(1000);
     }

}
