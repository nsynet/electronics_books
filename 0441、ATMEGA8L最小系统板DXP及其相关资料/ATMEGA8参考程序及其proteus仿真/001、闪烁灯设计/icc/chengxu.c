#include <iom8v.h>
/**********************************************************************
				  函数数据类型说明							   	
**********************************************************************/
#define uchar unsigned char 
#define uint unsigned int
/**********************************************************************
				  MS级延时函数程序，参数i 延时时间								   	
**********************************************************************/
void DelayMS(uint i)
{
uint j;
for(;i!=0;i--)
{
for(j=8000;j!=0;j--);
}
}
/**********************************************************************
				   主函数						   	
**********************************************************************/
void main(void)
{
 DDRB=0XFF;//端口设置，PB口设置为推挽1输出
 PORTB=0XFF;
 			while(1)//程序无条件执行
			{
			    PORTB=0X00;
			 	DelayMS(100);//延时时间约100MS
				PORTB=0XFF;
			 	DelayMS(100);//延时时间约100MS
			}
}
