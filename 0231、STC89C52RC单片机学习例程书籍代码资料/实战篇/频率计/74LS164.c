#include "stc.h"
#include "global.h"
#include "74LS164.h"

/*************************************************************
* 函数名称:LS164Send
* 输    入:无
* 输    出:无
* 说    明:74LS164 发送单个字节
**************************************************************/ 
void LS164Send(UINT8 byte)
{
   UINT8 j;

   for(j=0;j<=7;j++)
   {

     if(byte&(1<<(7-j)))
     {
	    LS164_DATA(HIGH);
	 }
	 else
	 {
	    LS164_DATA(LOW);
	 }

     LS164_CLK(LOW); 
     LS164_CLK(HIGH);
       
   }

}

