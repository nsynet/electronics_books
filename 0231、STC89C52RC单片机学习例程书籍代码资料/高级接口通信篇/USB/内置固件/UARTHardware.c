/******************************************************************
*函数名称:UARTHwInit
*输    入:无
*输    出:无
*说    明:串口 硬件层初始化
*******************************************************************/
void UARTHwInit(void)
{
  	SCON=0x40;
	T2CON=0x34;
	RCAP2L=0xD9;
	RCAP2H=0xFF;
	REN=1;


}
