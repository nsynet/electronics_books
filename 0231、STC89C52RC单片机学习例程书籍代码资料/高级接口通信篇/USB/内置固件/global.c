#include "stc.h"
#include "Global.h"
/****************************************************************************************************
*
*                                      变量区
* 
*****************************************************************************************************/
//系统消息变量
static
volatile UINT8 __ucSysMsg=SYS_IDLE;

/****************************************************
* 函数名称: DelayNus
* 输    入: t 延时时间
* 输    出: 无
* 功能描述: 微秒级延时
*****************************************************/
void DelayNus(UINT16 t)
{
	do
	{
		NOP();
	}while(--t >0);	
}

/****************************************************
* 函数名称:SYSIdle
* 输    入:无
* 输    出:无
* 说    明:空闲任务
****************************************************/
void SYSIdle(void)
{
	 MCU_IDLE();
}
/****************************************************
* 函数名称:SYSPostCurMsg
* 输    入:无
* 输    出:无
* 说    明:发送当前消息
****************************************************/
void  SYSPostCurMsg(UINT8 msg)
{
	__ucSysMsg=msg;
}
/****************************************************
* 函数名称:SYSRecvCurMsg
* 输    入:无
* 输    出:无
* 说    明:获取当前消息
****************************************************/
UINT8 SYSRecvCurMsg(void)
{
	return __ucSysMsg;
}
