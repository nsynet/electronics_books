#include<reg52.h>  	       //库文件
#define uchar unsigned char//宏定义无符号字符型
#define uint unsigned int  //宏定义无符号整型
/********************************************************************
                            I/O定义
*********************************************************************/
sbit LED=P1^0;	 //定义单片机P1口的第0位 （即P1.0）
/********************************************************************
                            延时函数
*********************************************************************/
void delay()//延时程序
{
uchar m,n,s;
for(m=20;m>0;m--)
for(n=20;n>0;n--)
for(s=248;s>0;s--);
}
/********************************************************************
                            主函数
*********************************************************************/
void main()
{
	while(1)   //无限循环
	{
	 LED=0;	   //点亮P1.0口灯
	 delay();  //延时
	 LED=1;	   //熄灭P1.0口灯
	 delay();  //延时
	}
}	
