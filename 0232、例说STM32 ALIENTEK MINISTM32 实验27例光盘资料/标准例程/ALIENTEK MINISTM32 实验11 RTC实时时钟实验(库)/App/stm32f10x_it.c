//RTC时钟中断
//每秒触发一次   
void RTC_IRQHandler(void)
	{							    
	if(RTC->CRL&0x0001)//秒钟中断
		{							
		RTC_Get();//更新时间 	 
		//printf("CRL:%d\n",RTC->CRL);	
		}
	if(RTC->CRL&0x0002)//闹钟中断
		{
		//printf("Alarm!\n");	
		RTC->CRL&=~(0x0002);//清闹钟中断
		//闹钟处理
		} 				  								 
	RTC->CRL&=0X0FFA;         //清除溢出，秒钟中断标志
	while(!(RTC->CRL&(1<<5)));//等待RTC寄存器操作完成		   							 	   	 
	}

